package com.whoami.po;

import java.util.Date;

public class items {
		private int itemsID;
		private String itemsName;
		private float itemsPrice;
		private int itemsAllNum;
		private int itemsLeaveNum;
		private Date itemsTime;
		private String image;
		private String itemsFactory;
		private String itemsAbout;
		private int itemsClassKey;
		public int getItemsID() {
			return itemsID;
		}
		public void setItemsID(int itemsID) {
			this.itemsID = itemsID;
		}
		public String getItemsName() {
			return itemsName;
		}
		public void setItemsName(String itemsName) {
			this.itemsName = itemsName;
		}
		public float getItemsPrice() {
			return itemsPrice;
		}
		public void setItemsPrice(float itemsPrice) {
			this.itemsPrice = itemsPrice;
		}
		public int getItemsAllNum() {
			return itemsAllNum;
		}
		public void setItemsAllNum(int itemsAllNum) {
			this.itemsAllNum = itemsAllNum;
		}
		public int getItemsLeaveNum() {
			return itemsLeaveNum;
		}
		public void setItemsLeaveNum(int itemsLeaveNum) {
			this.itemsLeaveNum = itemsLeaveNum;
		}
		public Date getItemsTime() {
			return itemsTime;
		}
		public void setItemsTime(Date itemsTime) {
			this.itemsTime = itemsTime;
		}
		public String getImage() {
			return image;
		}
		public void setImage(String image) {
			this.image = image;
		}
		public String getItemsFactory() {
			return itemsFactory;
		}
		public void setItemsFactory(String itemsFactory) {
			this.itemsFactory = itemsFactory;
		}
		public String getItemsAbout() {
			return itemsAbout;
		}
		public void setItemsAbout(String itemsAbout) {
			this.itemsAbout = itemsAbout;
		}
		public int getItemsClassKey() {
			return itemsClassKey;
		}
		public void setItemsClassKey(int itemsClassKey) {
			this.itemsClassKey = itemsClassKey;
		}
		
}
